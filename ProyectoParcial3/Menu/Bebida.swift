import UIKit

class Bebida {
    var fotoBebida: String
    var nombreBebida: String
    var precioBebida: String
    
    var fotoBebida2: String
    var nombreBebida2: String
    var precioBebida2: String
    
    init(fotoBebida: String, nombreBebida: String, precioBebida: String, fotoBebida2: String, nombreBebida2: String, precioBebida2: String) {
        self.fotoBebida = fotoBebida
        self.nombreBebida = nombreBebida
        self.precioBebida = precioBebida
        self.fotoBebida2 = fotoBebida2
        self.nombreBebida2 = nombreBebida2
        self.precioBebida2 = precioBebida2
    }
    
}
