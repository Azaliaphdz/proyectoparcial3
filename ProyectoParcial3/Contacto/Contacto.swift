class Contacto  {
    var nombre: String
    var cel: String
    var parentesco: String
    var fondoC: String
    var fotoC: String
    
    init(nombre: String, cel: String, parentesco: String, fondoC: String, fotoC: String) {
        self.nombre = nombre
        self.cel = cel
        self.parentesco = parentesco
        self.fondoC = fondoC
        self.fotoC = fotoC
    }
    
}
